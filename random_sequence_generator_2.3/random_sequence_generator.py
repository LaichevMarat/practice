import random


def random_sequence_generator():
    """
    –ü—Ä–æ–≥—Ä–∞–º–º–∞ –≥–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç —Å–ª—É—á–∞–π–Ω—ã–µ —á–∏—Å–ª–∞ –¥–æ —Ç–µ—Ö –ø–æ—Ä,
    –ø–æ–∫–∞ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –Ω–µ –≤–≤–µ–¥–µ—Ç 0.
    –í—ã–≤–æ–¥–∏—Ç –≤—Å–µ —á–∏—Å–ª–∞, –∫—Ä–æ–º–µ –ø–æ—Å–ª–µ–¥–Ω–µ–≥–æ (–Ω—É–ª—è).
    """
    print("=" * 60)
    print("–ì–ï–ù–ï–†–ê–¢–û–† –°–õ–£–ß–ê–ô–ù–´–• –ß–ò–°–ï–õ")
    print("–ü—Ä–æ–≥—Ä–∞–º–º–∞ –±—É–¥–µ—Ç –≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞—Ç—å —á–∏—Å–ª–∞ –ø–æ–∫–∞ –≤—ã –Ω–µ –≤–≤–µ–¥–µ—Ç–µ 0")
    print("=" * 60)

    generated_numbers = []  # –°–ø–∏—Å–æ–∫ –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è –≤—Å–µ—Ö —Å–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞–Ω–Ω—ã—Ö —á–∏—Å–µ–ª
    sequence_count = 0      # –°—á–µ—Ç—á–∏–∫ –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç–∏

    print("\n–ù–∞—á–∏–Ω–∞–µ–º –≥–µ–Ω–µ—Ä–∞—Ü–∏—é...")

    while True:
        # –ì–µ–Ω–µ—Ä–∞—Ü–∏—è —Å–ª—É—á–∞–π–Ω–æ–≥–æ —á–∏—Å–ª–∞ –æ—Ç 1 –¥–æ 100
        random_number = random.randint(1, 100)
        sequence_count += 1

        # –í—ã–≤–æ–¥ —Å–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞–Ω–Ω–æ–≥–æ —á–∏—Å–ª–∞
        print(f"üé≤ –°–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞–Ω–æ —á–∏—Å–ª–æ: {random_number}")

        # –î–æ–±–∞–≤–ª–µ–Ω–∏–µ —á–∏—Å–ª–∞ –≤ —Å–ø–∏—Å–æ–∫
        generated_numbers.append(random_number)

        # –ó–∞–ø—Ä–æ—Å –≤–≤–æ–¥–∞ –æ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        try:
            user_input = input("–í–≤–µ–¥–∏—Ç–µ –ª—é–±–æ–µ —á–∏—Å–ª–æ –¥–ª—è –ø—Ä–æ–¥–æ–ª–∂–µ–Ω–∏—è –∏–ª–∏ '0' –¥–ª—è –æ—Å—Ç–∞–Ω–æ–≤–∫–∏: ")

            # –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ –Ω–æ–ª—å
            if user_input.strip() == '0':
                print("\n" + "=" * 40)
                print("–û–±–Ω–∞—Ä—É–∂–µ–Ω –Ω–æ–ª—å! –ì–µ–Ω–µ—Ä–∞—Ü–∏—è –æ—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∞.")
                break

        except KeyboardInterrupt:
            print("\n\n–ü—Ä–æ–≥—Ä–∞–º–º–∞ –ø—Ä–µ—Ä–≤–∞–Ω–∞ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–º.")
            break
        except Exception as e:
            print(f"–ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞ –≤–≤–æ–¥–∞: {e}")
            continue

    # –í—ã–≤–æ–¥ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤
    print("\n" + "=" * 50)
    print("üìä –†–ï–ó–£–õ–¨–¢–ê–¢–´:")
    print(f"–í—Å–µ–≥–æ —Å–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞–Ω–æ —á–∏—Å–µ–ª: {sequence_count}")

    if generated_numbers:
        print(f"–í—Å–µ —Å–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞–Ω–Ω—ã–µ —á–∏—Å–ª–∞: {generated_numbers}")

        # –í—ã–≤–æ–¥ –≤—Å–µ—Ö —á–∏—Å–µ–ª –∫—Ä–æ–º–µ –ø–æ—Å–ª–µ–¥–Ω–µ–≥–æ (–Ω—É–ª—å –Ω–µ –≥–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç—Å—è, —Ç–∞–∫ —á—Ç–æ –ø–æ—Å–ª–µ–¥–Ω–µ–µ - —ç—Ç–æ –ø–æ—Å–ª–µ–¥–Ω–µ–µ —Å–ª—É—á–∞–π–Ω–æ–µ —á–∏—Å–ª–æ)
        numbers_except_last = generated_numbers[:-1] if len(generated_numbers) > 1 else []

        if numbers_except_last:
            print(f"–ß–∏—Å–ª–∞ –¥–ª—è –≤—ã–≤–æ–¥–∞: {numbers_except_last}")
            print(f"–ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –≤—ã–≤–µ–¥–µ–Ω–Ω—ã—Ö —á–∏—Å–µ–ª: {len(numbers_except_last)}")
        else:
            print("–ë—ã–ª–æ —Å–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞–Ω–æ —Ç–æ–ª—å–∫–æ –æ–¥–Ω–æ —á–∏—Å–ª–æ - –Ω–µ—á–µ–≥–æ –≤—ã–≤–æ–¥–∏—Ç—å.")
    else:
        print("–ß–∏—Å–ª–∞ –Ω–µ –±—ã–ª–∏ —Å–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞–Ω—ã.")

    print("=" * 50)


# –ó–∞–ø—É—Å–∫ –ø—Ä–æ–≥—Ä–∞–º–º—ã
if __name__ == "__main__":
    random_sequence_generator()
